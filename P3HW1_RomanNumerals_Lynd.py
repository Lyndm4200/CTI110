# Ask user to input a number 1-10
numEntered = int(input("Please enter a number 1-10: "))
# Determine the number entered and display the corresponding roman numeral
if numEntered == 1:
    print("I")
elif numEntered == 2:
    print("II")
elif numEntered == 3:
    print("III")
elif numEntered == 4:
    print("IV")
elif numEntered == 5:
    print("V")
elif numEntered == 6:
    print("VI")
elif numEntered == 7:
    print("VII")
elif numEntered == 8:
    print("VIII")
elif numEntered == 9:
    print("IX")
elif numEntered == 10:
    print("X")
else:
    print("ERROR: NUMBER INPUT INVALID")
